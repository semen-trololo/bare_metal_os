// Текущие цвета по умолчанию
static uint32_t current_fg = COLOR_LIGHT_GREY;
static uint32_t current_bg = COLOR_BLACK;

void fb_set_color(uint32_t fg, uint32_t bg) {
    current_fg = fg;
    current_bg = bg;
}

uint32_t fb_get_cols(void) {
    return fb_width / FONT_WIDTH;
}

uint32_t fb_get_rows(void) {
    return fb_height / FONT_HEIGHT;
}

void fb_set_cursor(uint32_t x, uint32_t y) {
    if (x < fb_get_cols()) cursor_x = x;
    if (y < fb_get_rows()) cursor_y = y;
}

// Скроллинг: сдвигаем всё содержимое вверх на одну строку
void fb_scroll_up(void) {
    if (!fb_ready) return;
    uint32_t font_px     = FONT_HEIGHT;                 // высота символа в пикселях
    uint32_t total_lines = fb_get_rows();
    
    // Копируем строки вверх: dst = src - font_px строк
    for (uint32_t line = 0; line < total_lines - 1; line++) {
        uint32_t src_y = (line + 1) * font_px;
        uint32_t dst_y = line * font_px;
        
        for (uint32_t row = 0; row < font_px; row++) {
            uint32_t* dst = &fb_addr[(dst_y + row) * fb_pitch];
            uint32_t* src = &fb_addr[(src_y + row) * fb_pitch];
            for (uint32_t col = 0; col < fb_width; col++) {
                dst[col] = src[col];
            }
        }
    }
    
    // Очищаем последнюю строку цветом фона
    uint32_t last_y = (total_lines - 1) * font_px;
    for (uint32_t row = 0; row < font_px; row++) {
        for (uint32_t col = 0; col < fb_width; col++) {
            fb_addr[(last_y + row) * fb_pitch + col] = current_bg;
        }
    }
}

// Потоковый вывод одного символа (аналог vga_putc)
void fb_putc(char c) {
    if (!fb_ready) return;
    
    uint32_t max_cols = fb_get_cols();
    uint32_t max_rows = fb_get_rows();
    
    switch (c) {
        case '\n':
            cursor_x = 0;
            cursor_y++;
            break;
        case '\r':
            cursor_x = 0;
            break;
        case '\t':
            // Табуляция: выравнивание по 4 символам
            cursor_x = (cursor_x + 4) & ~3;
            if (cursor_x >= max_cols) {
                cursor_x = 0;
                cursor_y++;
            }
            break;
        case '\b':
            if (cursor_x > 0) {
                cursor_x--;
                // Затереть символ пробелом
                fb_put_char(' ', cursor_x, cursor_y, current_fg, current_bg);
            }
            break;
        default:
            fb_put_char(c, cursor_x, cursor_y, current_fg, current_bg);
            cursor_x++;
            if (cursor_x >= max_cols) {
                cursor_x = 0;
                cursor_y++;
            }
            break;
    }
    
    // Автоскроллинг если ушли за нижнюю границу
    while (cursor_y >= max_rows) {
        fb_scroll_up();
        cursor_y--;
    }
}

void fb_print(const char* str) {
    if (!fb_ready || !str) return;
    while (*str) {
        fb_putc(*str++);
    }
}
