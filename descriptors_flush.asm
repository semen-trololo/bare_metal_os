; Ассемблерная заглушка для загрузки GDT в процессор
[bits 32]

; ============================================
; void gdt_flush(uint32_t gdt_ptr);
; ============================================
; Эта функция принимает указатель на структуру gdt_ptr
; и загружает её в процессор через инструкцию lgdt.
; Затем обновляет все сегментные регистры.
global gdt_flush
gdt_flush:
    ; Получаем аргумент из стека (первый параметр функции)
    ; В cdecl вызове аргументы лежат в стеке: [esp+4] = первый аргумент
    mov eax, [esp+4]
    
    ; Загружаем GDT в процессор
    ; lgdt ожидает 6-байтную структуру: 2 байта limit + 4 байта base
    lgdt [eax]
    
    ; ========================================
    ; Обновляем сегментные регистры данных
    ; ========================================
    ; 0x10 = селектор Kernel Data Segment (индекс 2 в GDT, 2*8=16=0x10)
    mov ax, 0x10          ; Загружаем селектор в AX
    mov ds, ax            ; Data Segment
    mov es, ax            ; Extra Segment
    mov fs, ax            ; Additional Segment
    mov gs, ax            ; Additional Segment
    mov ss, ax            ; Stack Segment
    
    ; ========================================
    ; Обновляем Code Segment через Far Jump
    ; ========================================
    ; CS регистр нельзя загрузить напрямую (mov cs, ax запрещён).
    ; Единственный способ — выполнить far jump на метку внутри кода.
    ; Far jump меняет и CS, и EIP одновременно.
    ; 0x08 = селектор Kernel Code Segment (индекс 1 в GDT, 1*8=8=0x08)
    jmp 0x08:.flush_done
    
.flush_done:
    ret                   ; Возврат из функции
; ============================================
; void idt_flush(uint32_t idt_ptr);
; ============================================
global idt_flush
idt_flush:
    mov eax, [esp+4]      ; Получаем указатель на idt_ptr
    lidt [eax]            ; Загружаем IDT в процессор
    ret