// --- 3. Низкоуровневая настройка VGA ---
static void vga_load_font() {
    // Перенастройка Graphics Controller для доступа к Plane 2
    outb(0x3CE, 0x05); outb(0x3CF, 0x00); // Отключаем Odd/Even
    outb(0x3CE, 0x06); outb(0x3CF, 0x04); // Окно памяти на 0xA0000
    
    // Настройка Sequencer для записи в Plane 2
    outb(0x3C4, 0x02); outb(0x3C5, 0x04); // Map Mask: только Plane 2
    outb(0x3C4, 0x04); outb(0x3C5, 0x06); // Отключаем Odd/Even
    
    // Загрузка шрифта (32 байта на символ в Plane 2)
    volatile uint8_t* plane2 = (volatile uint8_t*)0xA0000;
    for(int i = 0; i < 256; i++) {
        for(int j = 0; j < 8; j++) {
            plane2[i * 32 + j] = font8x8[i][j];
        }
    }
// Возврат к нормальному режиму
    outb(0x3C4, 0x02); outb(0x3C5, 0x03); // Map Mask: Planes 0 и 1
    outb(0x3C4, 0x04); outb(0x3C5, 0x02); // Odd/Even enabled
    outb(0x3CE, 0x05); outb(0x3CF, 0x10); // Odd/Even enabled
    outb(0x3CE, 0x06); outb(0x3CF, 0x0E); // Окно памяти на 0xB8000
}

static void vga_set_80x50() {
    // Разблокировка регистров CRTC (ПРАВИЛЬНОЕ снятие защиты)
    outb(0x3D4, 0x11);
    outb(0x3D5, inb(0x3D5) & 0x7F); 
    
    // Высота строки 8 пикселей
    outb(0x3D4, 0x09);
    outb(0x3D5, (inb(0x3D5) & 0xE0) | 0x07); // Сохраняем старшие биты
    
    // Курсор-подчеркивание внизу 8x8 ячейки
    outb(0x3D4, 0x0A); outb(0x3D5, 0x06); 
    outb(0x3D4, 0x0B); outb(0x3D5, 0x07); 
    
    // Выключение экрана
    outb(0x3C4, 0x01);
    uint8_t clk = inb(0x3C5);
    outb(0x3C5, clk | 0x20); 
    
    outb(0x3D4, 0x14);
    outb(0x3D5, 0x08); // Underline Location (Исправлено: выключаем подчеркивание)
    
    // Включение экрана
    outb(0x3C4, 0x01);
    outb(0x3C5, clk & ~0x20); 
}

void clear(void) {
    for(int i = 0; i < VGA_WIDTH * VGA_HEIGHT; i++) {
        vga_buffer[i] = (uint16_t)' ' | ((uint16_t)current_color << 8);
    }
    cursor_x = 0;
    cursor_y = 0;
    vga_update_hw_cursor(); 
}

void vga_set_color(uint8_t fg, uint8_t bg) {
    current_color = fg | (bg << 4);
}

void vga_init(void) {
    vga_set_color(VGA_COLOR_LIGHT_GREY, VGA_COLOR_BLACK); // Дефолтный цвет ДО очистки
    vga_load_font();
    vga_set_80x50();
    clear();
}

