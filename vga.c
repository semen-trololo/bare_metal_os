// Скрытые переменные состояния драйвера
static volatile uint16_t* vga_buffer = (volatile uint16_t*) 0xB8000;
static uint8_t current_color;
static uint8_t cursor_x;
static uint8_t cursor_y;

static void vga_scroll(void) {
    // Сдвигаем все строки на одну вверх
    for (int i = 0; i < (VGA_HEIGHT - 1) * VGA_WIDTH; i++) {
        vga_buffer[i] = vga_buffer[i + VGA_WIDTH];
    }
    // Очищаем последнюю строку
    for (int i = (VGA_HEIGHT - 1) * VGA_WIDTH; i < VGA_HEIGHT * VGA_WIDTH; i++) {
        vga_buffer[i] = (uint16_t)' ' | ((uint16_t)current_color << 8);
    }
    cursor_y = VGA_HEIGHT - 1;
}

static void vga_update_hw_cursor(void) {
    // Вычисляем линейную позицию курсора в текстовом буфере
    uint16_t pos = cursor_y * VGA_WIDTH + cursor_x;

    // Отправляем старший байт в регистр 0x0E (Cursor Location High)
    outb(0x3D4, 0x0E);
    outb(0x3D5, (uint8_t)((pos >> 8) & 0xFF));

    // Отправляем младший байт в регистр 0x0F (Cursor Location Low)
    outb(0x3D4, 0x0F);
    outb(0x3D5, (uint8_t)(pos & 0xFF));
}

void vga_putc(char c) {
    if (c == '\n') {
        cursor_x = 0;
        cursor_y++;
    } else if (c == '\r') {
        cursor_x = 0;
    } else if (c == '\b') {
        if (cursor_x > 0) {
            cursor_x--;
            int offset = cursor_y * VGA_WIDTH + cursor_x;
            vga_buffer[offset] = (uint16_t)' ' | ((uint16_t)current_color << 8);
        }
    } else if (c == '\t') {
        // 🌟 УМНАЯ ТАБУЛЯЦИЯ: выравниваем курсор до позиции, кратной 4
        int spaces = 4 - (cursor_x % 4);
        for (int i = 0; i < spaces; i++) {
            int offset = cursor_y * VGA_WIDTH + cursor_x;
            vga_buffer[offset] = (uint16_t)' ' | ((uint16_t)current_color << 8);
            cursor_x++;
            if (cursor_x >= VGA_WIDTH) {
                cursor_x = 0;
                cursor_y++;
            }
        }
    } else {
        int offset = cursor_y * VGA_WIDTH + cursor_x;
        vga_buffer[offset] = (uint16_t)(uint8_t)c | ((uint16_t)current_color << 8);
        cursor_x++;
        if (cursor_x >= VGA_WIDTH) {
            cursor_x = 0;
            cursor_y++;
        }
    }

    if (cursor_y >= VGA_HEIGHT) {
        vga_scroll();
    }

    // Синхронизируем аппаратный мигающий курсор
    vga_update_hw_cursor(); 
}


